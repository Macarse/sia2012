package com.g7;

public class Jar {
	private int maxLiters;
	private int used;

	public Jar(int maxLiters, int used) {
		this.maxLiters = maxLiters;
		this.used = used;
	}

	public int getMaxLiters() {
		return maxLiters;
	}

	public void setMaxLiters(int maxLiters) {
		this.maxLiters = maxLiters;
	}

	public int getUsed() {
		return used;
	}

	public void setUsed(int used) {
		this.used = used;
	}

	
}
